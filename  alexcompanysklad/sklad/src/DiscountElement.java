
public class DiscountElement {
	private int id_group;
	private int discount;
	public DiscountElement(int aId_group,int aDiscount){
		id_group=aId_group;
		discount=aDiscount;
	}
	public int getId_group() {
		return id_group;
	}
	public int getDiscount() {
		return discount;
	}
	
	
}
